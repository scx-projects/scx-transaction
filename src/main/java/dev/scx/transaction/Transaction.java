package dev.scx.transaction;

import dev.scx.function.Function0Void;
import dev.scx.transaction.exception.TransactionException;

/// Transaction
///
/// 表示一笔已经开始, 但尚未完成的活动事务.
///
/// 该对象同时承担两类职责：
/// 1. 事务结果的最终定案：通过 [#commit()] 或 [#rollback()] 决定该事务最终生效还是放弃.
/// 2. 事务句柄的生命周期结束：通过 [#close()] 结束该事务句柄并释放其底层资源.
///
/// 这里的 "事务" 是指某一底层资源管理域中的事务,
/// 例如同一个数据库连接, 同一个会话, 同一个存储运行时, 或其它能够提供事务语义的底层资源实现.
///
/// 该接口本身不规定底层如何实现事务,
/// 但约定 [#commit()], [#rollback()] 与 [#close()] 的语义必须稳定一致.
///
/// 事务对象通常由 [TransactionManager#begin()] 创建.
/// 被创建出来的事务在语义上已经 "开始" ,
/// 但是否参与某段具体执行, 取决于是否通过 [TransactionManager#with(Transaction, Function0Void)] 绑定到该执行作用域.
///
/// 约定：
/// 1. [#commit()] 表示提交事务.
/// 2. [#rollback()] 表示回滚事务.
/// 3. [#close()] 表示结束该事务句柄的生命周期并释放底层资源.
/// 4. 若事务尚未完成, 且此前未发生过 [#commit()] 或 [#rollback()] 失败,
///    则 [#close()] 应先执行回滚, 再释放底层资源.
/// 5. 若事务已经完成, 则 [#close()] 应为幂等操作, 通常应视为 no-op.
/// 6. 事务一旦完成（无论提交还是回滚）, 就不应再继续参与新的事务作用域, 也不应再次被提交或回滚.
/// 7. 若 [#commit()] 或 [#rollback()] 已经被调用但抛出异常, 则调用者不应再假定
///    该事务仍可继续参与新的事务语义操作;
///    此后通常只应再调用 [#close()] 进行最终收尾.
/// 8. 对于已经发生过 [#commit()] 或 [#rollback()] 失败的事务,
///    [#close()] 不要求再次尝试执行事务语义操作;
///    具体实现可直接进行资源释放, 或执行其认为安全的最终收尾策略.
///
/// 该接口不承诺：
///
/// 1. 跨不兼容资源域的统一事务语义.
/// 2. 分布式事务, 两阶段提交或跨多数据源一致性.
/// 3. 自动提交, 自动回滚或自动关闭.
///
/// @author scx567888
public interface Transaction extends AutoCloseable {

    /// 提交事务.
    ///
    /// 调用成功后, 表示该事务已经完成, 事务内的修改应按底层实现的语义正式生效.
    ///
    /// 语义要求：
    /// 1. [#commit()] 成功后, 该事务应进入 "已完成" 状态.
    /// 2. 提交成功后, 应结束该事务对底层事务资源的占用；是否立即物理释放资源, 由具体实现决定.
    /// 3. 事务一旦提交成功, 不应再继续参与新的 [TransactionManager#with(Transaction, Function0Void)] 作用域.
    /// 4. 对已经完成的事务再次调用 [#commit()], 通常应视为非法使用；具体实现可抛出异常或拒绝该操作.
    ///
    /// @throws TransactionException 当底层事务提交失败时抛出
    void commit() throws TransactionException;

    /// 回滚事务.
    ///
    /// 调用成功后, 表示该事务已经完成, 事务内尚未生效的修改应按底层实现的语义被放弃.
    ///
    /// 语义要求：
    /// 1. [#rollback()] 成功后, 该事务应进入 "已完成" 状态.
    /// 2. 回滚成功后, 应结束该事务对底层事务资源的占用；是否立即物理释放资源, 由具体实现决定.
    /// 3. 事务一旦回滚成功, 不应再继续参与新的 [TransactionManager#with(Transaction, Function0Void)] 作用域.
    /// 4. 对已经完成的事务再次调用 [#rollback()], 通常应视为非法使用；具体实现可抛出异常或拒绝该操作.
    ///
    /// @throws TransactionException 当底层事务回滚失败时抛出
    void rollback() throws TransactionException;

    /// 结束该事务句柄的生命周期, 并释放其底层资源.
    ///
    /// [#close()] 的职责不是将事务 "提交成功" ,
    /// 而是保证该事务句柄在离开使用范围时得到最终收尾,
    /// 不留下悬而未决的活动事务, 并使其底层资源得到妥善释放.
    ///
    /// 对于尚未完成, 且此前也未发生过 [#commit()] / [#rollback()] 失败的事务,
    /// [#close()] 应按失败路径终止该事务
    /// (通常表现为先执行 [#rollback()], 再释放底层资源).
    ///
    /// 对于已经完成的事务,
    /// [#close()] 仅表示结束句柄生命周期并执行资源收尾;
    /// 对调用者而言, 该操作通常应表现为幂等, 并可视为 no-op.
    ///
    /// 对于已经尝试过 [#commit()] 或 [#rollback()] 且抛出异常的事务,
    /// [#close()] 的职责仅在于进行最终收尾;
    /// 它不要求再次尝试执行事务语义操作
    /// (例如再次提交或再次回滚).
    /// 具体实现可直接释放资源, 或执行其认为安全的最终收尾策略.
    ///
    /// 语义要求:
    /// 1. 若事务尚未完成, 且此前未发生过 [#commit()] / [#rollback()] 失败,
    ///    则 [#close()] 应先放弃该事务, 再释放底层资源.
    ///    在大多数实现中, 这通常等价于先执行 [#rollback()], 再进行资源释放.
    /// 2. 若事务已经完成, 则 [#close()] 应仅执行资源收尾;
    ///    对调用者而言, 该操作通常应表现为幂等, 并可视为 no-op.
    /// 3. 若事务此前在 [#commit()] 或 [#rollback()] 时已经抛出异常,
    ///    则 [#close()] 不要求再次尝试提交或回滚;
    ///    此时它的主要职责是完成最终收尾与资源释放.
    /// 4. 调用 [#close()] 后, 该事务句柄的生命周期即告结束,
    ///    不应再继续参与新的事务作用域, 也不应再被提交或回滚.
    ///
    /// 因此, 一个典型用法通常是:
    /// - 成功路径显式调用 [#commit()]
    /// - 失败路径显式调用 [#rollback()]
    /// - 最外层通过 try-with-resources 或 finally 确保 [#close()] 最终被调用
    ///
    /// @throws TransactionException 当最终收尾或底层资源释放失败时抛出
    @Override
    void close() throws TransactionException;

}
